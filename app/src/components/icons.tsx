export function Icon({ name, className = '', size = 24 }: IconProps) {
  const IconComponent = icons[name.toLowerCase()];
  
  if (!IconComponent) {
    console.warn(`Icon "${name}" not found`);
    return null;
  }
  
  return <IconComponent className={className} size={size} />;
}

export function getIconColorClasses(color: string): string {
  const colorMap: Record<string, string> = {
    'purple': 'bg-violet-500/10 text-violet-400',
    'pink': 'bg-pink-500/10 text-pink-400',
    'teal': 'bg-cyan-500/10 text-cyan-400',
    'orange': 'bg-amber-500/10 text-amber-400',
  };
  return colorMap[color] || 'bg-zinc-800 text-muted';
}
